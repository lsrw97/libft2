#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		i;

	i = 0;
	if (fd >= 0)
	{
		while (s[0] != '\0')
		{
			write(fd, s, 1);
			s++;
			i++;
		}
		s -= i;
	}
}
