int	ft_atoi(const char *nptr)
{
	int	i;
	int	num;

	i = 0;
	num = 0;
	while (nptr[i] != '\0' && nptr[i] >= '0' && nptr[i] <= '9')
	{
		if (num == 0)
			num += nptr[i] - 48;
		else
			num = num * 10 + nptr[i] - 48;
		i++;
	}
	return (num);
}
